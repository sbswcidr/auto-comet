package org.auto.xml;

/**
 *
 * @author XiaohangHu
 */
public class DocumentLoadException extends RuntimeException {

	/**
	 *
	 */
	private static final long serialVersionUID = 5982113877670286969L;

	public DocumentLoadException() {
		super();
	}

	public DocumentLoadException(String message) {
		super(message);
	}

	public DocumentLoadException(String message, Throwable cause) {
		super(message, cause);
	}

}
